class picture{
    private :
    
    public:
        int width;
        int height;
        vector<string> data;
    picture(vector<string> data){
        this->data = data;
        this->width = 0;
        for(auto& x : data){
            if(x.size() > this->width){
                this->width = x.size();
            }
        }
        this->height = data.size();
    }

    int get_width(){
        return width;
    }

    int get_height(){
        return height;
    }

    void print(){
        for(auto& x : data){
            cout << x << endl;
        }
    }

    vector<string> get_data(){
        return data;
    }

    void hflip(){
        for(auto& x : data){
            reverse(x.begin(), x.end());
        }
    }

    void vflip(){
        reverse(data.begin(), data.end());
    }
    
    picture hcat(vector<string> pic){
        vector<string> temp = this->data;
        for(int i = 0; i < pic.size(); i++){
            temp[i] += pic[i];
        }
        return picture(temp);
    }

    picture vcat(vector<string> pic){
        vector<string> temp = this->data;
        for(auto& x : pic){
            temp.push_back(x);
        }
        return picture(temp);
    }

    void fill(){
        for(auto& x : data){
            if(x.size() < width){
                x += string(width - x.size(), '_');
            }
        }
    }

    void resize(int w , int h){
        if(w > width){
            for(auto& x : data){
                x += string(w - x.size(), '_');
            }
        }
        else{
            for(auto& x : data){
                x = x.substr(0, w);
            }
        }
        if(h > height){
            for(int i = 0; i < h - height; i++){
                data.push_back(string(w, '_'));
            }

        }
        else{
            data = vector<string>(data.begin(), data.begin() + h);
        }
        width = w;
        height = h;
    }

    
};

